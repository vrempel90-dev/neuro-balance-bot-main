# Backward-compatible name for old imports.
CLINIC_INFO_TEMPLATES = CLINIC_INFO_TEMPLATES_RU


def get_clinic_info(topic: str, lang: str | None = None) -> str | None:
    """Return clinic template in requested language.

    lang="kk" returns Kazakh template when available. All old calls without lang
    still work and return Russian template, so imports do not break.
    """
    topic = str(topic or "")
    if lang == "kk":
        return CLINIC_INFO_TEMPLATES_KK.get(topic) or CLINIC_INFO_TEMPLATES_RU.get(topic)
    return CLINIC_INFO_TEMPLATES_RU.get(topic)


def topics() -> list[str]:
    return list(CLINIC_INFO_TEMPLATES_RU.keys())
